name = "sajib"
print(len(name)) # print the string length
print(name.find("j")) # print the index no. of the character in the string
print(name.capitalize()) # capitalize the first character
print(name.upper()) # upper case all the character of the string
print(name.lower()) # lower case all the character of the string
print(name.isdigit()) # return false
print(name.isalpha()) # rerurn true if the character is alpha
print(name.count("i")) # it will count the no. of i present in the string
print(name.replace("s","A")) # it will replace s to A
print(name*3) # it will print the name (sajib) 3 times.